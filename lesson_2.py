# Lesson 2 - Working with strings

my_name = "Ruban Raghavendar"

print(my_name.upper()) # change to upper case
print(my_name.lower()) # change to lower case
print(my_name.capitalize()) # change to first letter upper  
print(my_name.upper().isupper()) # check whether it is upper or not
print(my_name.upper().islower()) # check whether it is lower or not
print(len(my_name)) # it'll tell the total characters in that string including whitespaces


print(my_name.replace("Raghavendar","Arthi"))